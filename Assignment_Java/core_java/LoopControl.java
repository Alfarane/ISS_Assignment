public class LoopControl {
    public static void main(String[] args) {

        // If-else statement
        int number = 10;

        if (number > 0) {
            System.out.println("Positive");
        } else {
            System.out.println("Negative");
        }

        // For loop
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }

        // While loop
        int i = 1;
        while (i <= 5) {
            System.out.println(i);
            i++;
        }
    }
}
